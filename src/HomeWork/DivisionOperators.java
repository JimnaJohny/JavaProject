package HomeWork;

public class DivisionOperators {
	int a;
	int b;
	void divisionOfNumbers() {
		int quotient = a / b;
		System.out.println("Quotient of " + a + " and " + b + " is " + quotient);
		int remainder = a % b;
		System.out.println("Remainder of the division operation of " + a + " and " + b + " is " + remainder);
	}

}
